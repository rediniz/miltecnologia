    private boolean validarCampos() {
        if (field_nomeFuncionario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o nome do funcionário", "Mensagem", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (field_dataEntrada.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe a data de entrada na empresa.", "Mensagem", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (field_dataNascimento.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe a data de nascimento do funcionário.", "Mensagem", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (field_salario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o salário do funcionário.", "Mensagem", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
