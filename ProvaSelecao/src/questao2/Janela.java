    private void button_CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_CalcularActionPerformed
        if (validarCampos()) {
            String nome = field_nomeFuncionario.getText();
            String dataEntrada = field_dataEntrada.getText();
            String dataNascimento = field_dataNascimento.getText();
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");

            double gratificacao = 0;
            Date dataEntradaf = null;
            Date dataNascimentof = null;
            try {
                dataEntradaf = format.parse(dataEntrada);
                dataNascimentof = format.parse(dataNascimento);
            } catch (ParseException ex) {
                Logger.getLogger(Janela.class.getName()).log(Level.SEVERE, null, ex);
            }
            double salario = Double.parseDouble(field_salario.getText());

            Calendar entrada = Calendar.getInstance();
            entrada.setTime(dataEntradaf);

            Calendar nascimento = Calendar.getInstance();
            nascimento.setTime(dataNascimentof);

            int anoEntrada = entrada.get(Calendar.YEAR);
            int anoNascimento = nascimento.get(Calendar.YEAR);
            int anoAtual = Calendar.getInstance().get(Calendar.YEAR);

            int diferenca = anoAtual - anoEntrada;
            int idade = anoAtual - anoNascimento;

            if (diferenca >= 10) {
                
                gratificacao = (0.15 * salario);
            } else if (diferenca >= 5 && diferenca <= 7) {

                gratificacao = 0.102 * salario;
            } else if (diferenca <= 3) {

                gratificacao = 0.102 * salario;
            }

            if (idade >= 50) {
                gratificacao = gratificacao + (0.12 * gratificacao);
            }
            
            label_Resultado.setText(String.valueOf(gratificacao));
        }
    }//GEN-LAST:event_button_CalcularActionPerformed

